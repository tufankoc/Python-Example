#Escape Sequences
# \n new line yeni satır
print("Hello \n World") 
# \t tab boşluk
print("Hello \t World")
# \b backspace geri
print("Hello \b World") 
# \r carriage return 
print("Hello \r World")
# \f form feed bir sayfa sonu
print("Hello \f World")
# \v vertical tab boşluk
print("Hello \v World")
# \a alert alarm ses
print("Hello \a World")
# \000 null karakter
print("Hello \000 World")
# \xhh hexadecimal karakter
print("Hello \x00 World")
# \uhhhh Unicode karakter 
print("Hello \u0020 World")
# \N{name} Unicode karakter ismi
print("Hello \N{SPACE} World")